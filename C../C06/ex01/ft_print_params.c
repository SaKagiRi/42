#include <unistd.h>

void	print(char *str)
{
	while (*str)
		write(1, str++, 1);
	write(1, "\n", 1);
}

int	main(int c, char **v)
{
	int	i;

	i = 1;
	while (i < c)
		print(v[i++]);
}
