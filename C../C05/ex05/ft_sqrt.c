int	ft_sqrt(int nb)
{
	int	i;
	int	j;

	i = 1;
	j = 1;
	if (nb == 0)
		return (0);
	while (nb != 0)
	{
		nb -= j;
		if (nb == 0)
			break ;
		else if (nb < 0)
			return (0);
		i++;
		j += 2;
	}
	return (i);
}
